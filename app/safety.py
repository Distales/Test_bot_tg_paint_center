import re
from typing import Tuple

def is_company_related(question: str) -> bool:
    """
    Простая эвристика для проверки, относится ли вопрос к компании.
    Это запасной вариант - основная проверка будет через AI.
    
    Args:
        question (str): Вопрос пользователя
        
    Returns:
        bool: True, если вопрос likely связан с компанией, иначе False
    """
    # Приводим к нижнему регистру и убираем пробелы по краям для удобного поиска
    question_lower = question.lower().strip()
    
    # Список ключевых слов, связанных с компанией (на русском и частично на английском)
    company_keywords = [
        # Инфо о компании
        'центр красок', 'centr Krasok', 'центр красок #1', 'samruk trade',
        'ТОО SAMRUk Trade', 'бизнес', 'компания', 'фирма', 'организация',
        
        # Товары
        'краска', 'краски', 'покраска', 'лак', 'лаки', 'масло', 'масла',
        'грунтовка', 'грунтовки', 'антисептик', 'антисептики', 'пропитка',
        'пропитки', 'штукатурка', 'штукатурки', 'декоративный', 'фактурный',
        'аэрозоль', 'аэрозоли', 'обои', 'монтажная пена', 'монтажные пены',
        'шпатлевка', 'шпатлевки', 'растворитель', 'растворители',
        'очиститель', 'очистители', 'инструмент', 'инструменты',
        'валик', 'валики', 'кисть', 'кисти', 'ролик', 'ролики',
        'наждачная бумага', 'кельма', 'шпатель', 'лента', 'скотч',
        'изолента', 'диспенсер', 'краскопульт',
        
        # Услуги
        'доставка', 'самовывоз', 'подбор цвета', 'колеровка', 'колеровка',
        'консультация', 'консультации', 'эксперт', 'дизайнер',
        'строитель', 'ремонтная бригада', 'бригада',
        
        # Локации
        'алматы', 'астана', 'адрес', 'адреса', 'магазин', 'шоурум',
        'офис', 'точка продаж', 'ул.', 'улица', 'бутик',
        
        # Бренды
        'dulux', 'pinotex', 'hammerite', 'marshall', 'master color',
        'oikos', 'бренд', 'бренды',
        
        # Соцсети
        'instagram', 'инстаграм', 'youtube', 'ютуб', '2gis',
        
        # Общее
        'товар', 'товары', 'услуга', 'услуги', 'цена', 'цены',
        'наличие', 'акция', 'акции', 'скидка', 'скидки',
        'вакансия', 'вакансии', 'работа', 'трудоустройство'
    ]
    
    # Проверяем, есть ли хотя бы одно ключевое слово в вопросе
    for keyword in company_keywords:
        if keyword in question_lower:
            return True
    
    # Дополнительные паттерны (регулярные выражения) для захвата фраз
    patterns = [
        r'\bгде\b.*\bнаходится\b',  # например, "где находится"
        r'\bкак\b.*\bдобраться\b',  # например, "как добраться"
        r'\bчат\b',  # просто наличие слова "чат"
        r'\bбот\b',  # просто наличие слова "бот"
    ]
    
    for pattern in patterns:
        if re.search(pattern, question_lower, re.IGNORECASE):
            return True
    
    # Проверяем наличие кириллических символов (русский/казахский)
    if any('\u0400' <= ch <= '\u04FF' for ch in question_lower):
        return True
    
    # Если вопрос очень короткий (меньше 2 слов), может быть приветствие или уточнение
    if len(question_lower.split()) < 2:
        # Список типичных приветствий
        greetings = ['привет', 'hello', 'hi', 'хай', 'здравствуй', 'добрый день', 'добрый вечер']
        if any(greeting in question_lower for greeting in greetings):
            return True
        # Или короткие вопросительные слова
        short_questions = ['да', 'нет', 'кто', 'что', 'где', 'когда', 'почему', 'как']
        if any(q in question_lower for q in short_questions):
            return True
    
    # Ничего не подошло - считаем, что вопрос не related к компании
    return False

